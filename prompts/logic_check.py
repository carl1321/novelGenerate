"""
逻辑检查Prompt模板
"""

def get_logic_check_prompt(content: str) -> str:
    """
    获取逻辑检查prompt
    
    Args:
        content: 待检查内容
    
    Returns:
        格式化的prompt字符串
    """
    return f"""你是一个专业的逻辑分析师，专门检查修仙小说内容的逻辑一致性。

待检查内容：{content}

## 检查维度

### 1. 修炼体系逻辑
**检查要点**:
- 境界提升是否符合修炼规律
- 功法使用是否符合境界要求
- 灵根属性是否与功法匹配
- 修炼时间是否合理
- 天劫设定是否合理

**常见问题**:
- 境界提升过快或过慢
- 低境界使用高等级功法
- 灵根属性与功法不匹配
- 修炼时间不符合设定
- 天劫威力不合理

### 2. 角色行为逻辑
**检查要点**:
- 角色行为是否符合其性格
- 角色决定是否符合其目标
- 角色能力是否与其境界匹配
- 角色关系是否合理
- 角色成长是否自然

**常见问题**:
- 角色行为前后不一致
- 角色决定不符合其性格
- 角色能力与境界不匹配
- 角色关系发展不合理
- 角色成长过于突兀

### 3. 世界观逻辑
**检查要点**:
- 世界观规则是否一致
- 地理设定是否合理
- 历史背景是否自洽
- 文化设定是否符合逻辑
- 门派组织是否合理

**常见问题**:
- 世界观规则前后矛盾
- 地理设定不符合逻辑
- 历史背景有漏洞
- 文化设定不合理
- 门派组织过于简单

### 4. 时间线逻辑
**检查要点**:
- 时间顺序是否正确
- 事件发生时间是否合理
- 角色年龄是否一致
- 修炼进度是否符合时间
- 历史事件时间是否合理

**常见问题**:
- 时间顺序混乱
- 事件发生时间不合理
- 角色年龄前后不一致
- 修炼进度过快或过慢
- 历史事件时间有误

### 5. 因果关系逻辑
**检查要点**:
- 事件之间是否有因果关系
- 结果是否符合原因
- 角色动机是否充分
- 冲突产生是否合理
- 问题解决是否自然

**常见问题**:
- 事件之间缺乏因果关系
- 结果不符合原因
- 角色动机不充分
- 冲突产生不合理
- 问题解决过于简单

## 输出格式

请严格按照以下JSON格式输出检查结果：

```json
{{
  "overall_status": "整体逻辑状态",
  "issues_found": [
    {{
      "category": "问题分类",
      "severity": "严重程度",
      "description": "问题描述",
      "location": "问题位置",
      "suggestion": "修改建议"
    }}
  ],
  "dimension_scores": {{
    "cultivation_logic": 修炼体系逻辑分数,
    "character_logic": 角色行为逻辑分数,
    "world_logic": 世界观逻辑分数,
    "timeline_logic": 时间线逻辑分数,
    "causality_logic": 因果关系逻辑分数
  }},
  "summary": "总体评价",
  "recommendations": ["建议1", "建议2", "建议3"]
}}
```

## 注意事项

1. 检查要全面细致，不能遗漏重要问题
2. 问题描述要具体明确，不能过于笼统
3. 修改建议要具体可行，不能过于抽象
4. 严重程度要客观评估，不能主观臆断
5. 总体评价要综合各维度，给出平衡的判断"""
