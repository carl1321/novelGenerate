"""
评分标准Prompt模板
"""

def get_scoring_criteria_prompt(content: str, dimension: str = "all") -> str:
    """
    获取评分标准prompt
    
    Args:
        content: 待评分内容
        dimension: 评分维度
    
    Returns:
        格式化的prompt字符串
    """
    return f"""你是一个专业的内容评分员。请根据以下标准对修仙小说内容进行多维度评分。

待评分内容：{content}
评分维度：{dimension}

## 评分维度

### 1. 逻辑自洽性 (30%)
**评分标准**:
- 10分：逻辑完全自洽，无任何矛盾
- 8-9分：基本自洽，有轻微逻辑问题
- 6-7分：存在一些逻辑问题，但不影响整体
- 4-5分：逻辑问题较多，影响阅读体验
- 1-3分：逻辑混乱，前后矛盾严重

**检查要点**:
- 修炼境界提升是否合理
- 角色行为是否符合其设定
- 时间线是否一致
- 因果关系是否成立
- 世界观规则是否被遵守

### 2. 戏剧冲突性 (20%)
**评分标准**:
- 10分：冲突激烈，张力十足，扣人心弦
- 8-9分：冲突明显，有足够的戏剧张力
- 6-7分：冲突一般，有一定的紧张感
- 4-5分：冲突较弱，缺乏紧张感
- 1-3分：缺乏冲突，平淡无奇

**检查要点**:
- 主要冲突是否明确
- 冲突升级是否合理
- 角色动机是否充分
- 紧张感是否持续
- 冲突解决是否合理

### 3. 角色塑造 (20%)
**评分标准**:
- 10分：角色立体丰满，性格鲜明，成长明显
- 8-9分：角色较为立体，性格清晰，有成长
- 6-7分：角色基本立体，性格一般，成长有限
- 4-5分：角色较为平面，性格模糊，缺乏成长
- 1-3分：角色单薄，性格不明确，无成长

**检查要点**:
- 角色性格是否鲜明
- 角色动机是否合理
- 角色成长是否明显
- 角色关系是否复杂
- 角色对话是否个性化

### 4. 世界观运用 (15%)
**评分标准**:
- 10分：世界观运用巧妙，设定丰富，逻辑严密
- 8-9分：世界观运用较好，设定较为丰富
- 6-7分：世界观运用一般，设定基本合理
- 4-5分：世界观运用较差，设定较为简单
- 1-3分：世界观运用很差，设定混乱

**检查要点**:
- 修炼体系是否合理
- 门派设定是否丰富
- 地理环境是否详细
- 历史背景是否完整
- 文化设定是否深入

### 5. 文笔表达 (15%)
**评分标准**:
- 10分：文笔优美，表达生动，语言流畅
- 8-9分：文笔较好，表达清晰，语言通顺
- 6-7分：文笔一般，表达基本清晰
- 4-5分：文笔较差，表达不够清晰
- 1-3分：文笔很差，表达混乱

**检查要点**:
- 语言是否流畅
- 描写是否生动
- 对话是否自然
- 叙述是否清晰
- 修辞是否恰当

## 输出格式

请严格按照以下JSON格式输出评分结果：

```json
{{
  "total_score": 总分,
  "scores": {{
    "logic_consistency": 逻辑自洽性分数,
    "dramatic_conflict": 戏剧冲突性分数,
    "character_development": 角色塑造分数,
    "world_usage": 世界观运用分数,
    "writing_style": 文笔表达分数
  }},
  "detailed_feedback": {{
    "logic_consistency": "逻辑自洽性详细反馈",
    "dramatic_conflict": "戏剧冲突性详细反馈",
    "character_development": "角色塑造详细反馈",
    "world_usage": "世界观运用详细反馈",
    "writing_style": "文笔表达详细反馈"
  }},
  "overall_feedback": "总体评价和建议",
  "improvement_suggestions": ["改进建议1", "改进建议2", "改进建议3"]
}}
```

## 注意事项

1. 评分要客观公正，不能带有个人偏见
2. 每个维度都要给出具体分数和详细反馈
3. 改进建议要具体可行，不能过于笼统
4. 总分要反映各维度的加权平均
5. 反馈要指出具体问题和改进方向"""
