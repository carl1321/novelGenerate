"""
评分标准Prompt模板
"""

def get_scoring_criteria_prompt(content: str, dimension: str = "all") -> str:
    """
    获取评分标准prompt
    
    Args:
        content: 待评分内容
        dimension: 评分维度
    
    Returns:
        格式化的prompt字符串
    """
    return f"""你是一个专业的内容评分员。请根据以下标准对修仙小说内容进行多维度评分。

待评分内容：{content}
评分维度：{dimension}

## 评分维度

### 1. 戏剧张力 (Dramatic Tension) (25%)
**评分标准**:
- 90-100分：紧张刺激，悬念十足，扣人心弦，读者无法放下
- 80-89分：紧张感明显，悬念较好，能吸引读者继续阅读
- 70-79分：有一定的紧张感，悬念一般，基本能保持读者兴趣
- 60-69分：紧张感较弱，悬念不足，读者容易失去兴趣
- 0-59分：缺乏紧张感，无悬念，平淡无奇

**检查要点**:
- 事件是否具有足够的紧张刺激感
- 悬念设置是否巧妙且持续
- 冲突升级是否合理且引人入胜
- 节奏控制是否恰当
- 读者是否会产生"接下来会怎样"的期待

### 2. 情感冲击 (Emotional Impact) (20%)
**评分标准**:
- 90-100分：情感冲击强烈，能引起读者强烈的情感波动和共鸣
- 80-89分：情感冲击明显，能引起读者明显的情感反应
- 70-79分：有一定的情感冲击，能引起读者轻微的情感反应
- 60-69分：情感冲击较弱，读者反应平淡
- 0-59分：缺乏情感冲击，读者无动于衷

**检查要点**:
- 是否能引起读者的悲伤、喜悦、愤怒、恐惧等情感
- 情感表达是否真实可信
- 情感转折是否自然合理
- 读者是否能产生共鸣
- 情感强度是否适中

### 3. 角色塑造 (Character Development) (20%)
**评分标准**:
- 90-100分：角色立体丰满，性格鲜明，成长明显，层次丰富
- 80-89分：角色较为立体，性格清晰，有成长，层次较好
- 70-79分：角色基本立体，性格一般，成长有限，层次一般
- 60-69分：角色较为平面，性格模糊，缺乏成长，层次简单
- 0-59分：角色单薄，性格不明确，无成长，层次单一

**检查要点**:
- 角色性格是否鲜明且一致
- 角色动机是否合理且充分
- 角色成长是否明显且合理
- 角色关系是否复杂且真实
- 角色对话是否个性化且符合身份

### 4. 主题深度 (Thematic Depth) (15%)
**评分标准**:
- 90-100分：主题深刻，内涵丰富，能引发读者深度思考
- 80-89分：主题较为深刻，有一定的内涵，能引发思考
- 70-79分：主题一般，内涵有限，思考价值一般
- 60-69分：主题浅显，内涵不足，缺乏思考价值
- 0-59分：主题模糊，无内涵，无思考价值

**检查要点**:
- 是否深化了故事的核心主题
- 主题表达是否自然不刻意
- 是否能引发读者的深度思考
- 主题与情节的结合是否紧密
- 主题的现实意义和价值

### 5. 节奏与流畅度 (Pacing & Fluency) (10%)
**评分标准**:
- 90-100分：节奏完美，流畅度极佳，阅读体验极佳
- 80-89分：节奏良好，流畅度较好，阅读体验良好
- 70-79分：节奏一般，流畅度一般，阅读体验一般
- 60-69分：节奏较差，流畅度不足，阅读体验较差
- 0-59分：节奏混乱，流畅度很差，阅读体验很差

**检查要点**:
- 事件节奏是否合适（快慢适中）
- 叙述是否流畅自然
- 段落转换是否自然
- 语言表达是否清晰
- 阅读体验是否舒适

### 6. 新颖性与创意 (Originality & Creativity) (10%)
**评分标准**:
- 90-100分：极具创意，令人耳目一新，有独特的亮点
- 80-89分：创意较好，有一定的新颖性，有亮点
- 70-79分：创意一般，新颖性有限，亮点不多
- 60-69分：创意不足，较为老套，缺乏亮点
- 0-59分：缺乏创意，完全老套，无任何亮点

**检查要点**:
- 是否避免了常见的套路和桥段
- 是否有令人耳目一新的创意点
- 情节设置是否独特
- 角色设定是否有新意
- 整体构思是否有创新性

## 输出格式

请严格按照以下JSON格式输出评分结果：

```json
{{
  "total_score": 总分,
  "scores": {{
    "dramatic_tension": 戏剧张力分数,
    "emotional_impact": 情感冲击分数,
    "character_development": 角色塑造分数,
    "thematic_depth": 主题深度分数,
    "pacing_fluency": 节奏与流畅度分数,
    "originality_creativity": 新颖性与创意分数
  }},
  "detailed_feedback": {{
    "dramatic_tension": "戏剧张力详细反馈",
    "emotional_impact": "情感冲击详细反馈",
    "character_development": "角色塑造详细反馈",
    "thematic_depth": "主题深度详细反馈",
    "pacing_fluency": "节奏与流畅度详细反馈",
    "originality_creativity": "新颖性与创意详细反馈"
  }},
  "overall_feedback": "总体评价和建议",
  "improvement_suggestions": ["改进建议1", "改进建议2", "改进建议3"]
}}
```

## 注意事项

1. 评分要客观公正，不能带有个人偏见
2. 每个维度都要给出具体分数和详细反馈
3. 改进建议要具体可行，不能过于笼统
4. 总分要反映各维度的加权平均
5. 反馈要指出具体问题和改进方向"""
